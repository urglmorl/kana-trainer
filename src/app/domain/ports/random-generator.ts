/**
 * Порт для генератора случайных элементов.
 * Используется для выбора следующего элемента в тренировке.
 */
export interface IRandomGenerator<T> {
  /**
   * Инициализирует генератор с указанным пулом элементов.
   * @param pool - Массив элементов для случайного выбора.
   */
  init(pool: readonly T[]): void;

  /**
   * Возвращает следующий случайный элемент.
   * Гарантирует отсутствие повторений подряд (если pool.length > 1).
   * @returns Следующий элемент.
   */
  next(): T;

  /**
   * Проверяет, инициализирован ли генератор.
   */
  isInitialized(): boolean;
}

